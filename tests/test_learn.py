lights = [(0,   0), (1800,   0), (3600,   0), (5400,   0),
          (0, 400), (1800, 400), (3600, 400), (5400, 400),
          (0, 800), (1800, 800), (3600, 800), (5400, 800)]

desks = [(1000, 2000), (2200, 2000), (3400, 2000),
         (1000, 2400), (2200, 2400), (3400, 2400)]

for desk in desks:
    dlight = 0
    for light in lights:
        dlight += cd2lx(desk, light, 500)
    print(dlight)